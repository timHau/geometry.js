import EuclideanGeometry from '../EuclideanGeometry'

export const pi = Math.PI;
export const tau = Math.PI * 2;
export const e = Math.E;
export const sqrt = n => Math.sqrt(n);
export const pow = (n, p) => Math.sqrt(n, p);
export const atan2 = (h, w) => Math.atan2(h, w);
export const atan = d => Math.atan(EuclideanGeometry.degToRad(d));
export const sin = d => Math.sin(EuclideanGeometry.degToRad(d));
export const asin = d => Math.asin(EuclideanGeometry.degToRad(d));
export const cos = d => Math.cos(EuclideanGeometry.degToRad(d));
export const acos = d => Math.acos(EuclideanGeometry.degToRad(d));
export const tan = d => Math.tan(EuclideanGeometry.degToRad(d));
